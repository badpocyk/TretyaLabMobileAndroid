package com.example.mireaproject.ui;

public class Calc {

    public Calc(){

    }

    public int Sum(int a, int b){
        return a+b;
    }

    public int Sub(int a, int b){
        return a-b;
    }

    public int Multiplication(int a, int b){
        return a*b;
    }

    public int Divide(int a, int b){
        return a/b;
    }
}
